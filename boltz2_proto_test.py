import py3Dmol

def get_structure_text(structure):
    """Return (text, fmt) for a proto_tools structure, trying common export methods."""
    for meth, fmt in [("to_pdb", "pdb"), ("to_cif", "cif"),
                      ("to_mmcif", "cif"), ("pdb_string", "pdb"), ("cif_string", "cif")]:
        obj = getattr(structure, meth, None)
        if obj is None:
            continue
        return (obj() if callable(obj) else obj), fmt
    # fallback: write to disk then read back
    for meth, fmt in [("write_pdb", "pdb"), ("save_pdb", "pdb"), ("write_cif", "cif")]:
        if hasattr(structure, meth):
            path = f"/tmp/pred.{fmt}"
            getattr(structure, meth)(path)
            return open(path).read(), fmt
    raise RuntimeError("Couldn't export structure — check dir(structure) for the right method.")


def visualize_with_mutation(structure, highlight_resi, chain="A", width=800, height=600):
    """
    Cartoon colored by B-factor (pLDDT), with the mutated residue(s) highlighted.
    highlight_resi: int or list of ints (1-based residue numbers), e.g. 711 or [1787, 1788]
    """
    if isinstance(highlight_resi, int):
        highlight_resi = [highlight_resi]

    text, fmt = get_structure_text(structure)
    sel = {"chain": chain, "resi": highlight_resi}

    view = py3Dmol.view(width=width, height=height)
    view.addModel(text, fmt)

    # base: cartoon colored by pLDDT (stored in B-factor), 0-100 low->high
    view.setStyle({}, {"cartoon": {
        "colorscheme": {"prop": "b", "gradient": "roygb", "min": 0, "max": 100}
    }})

    # highlight the mutated residue(s)
    view.addStyle(sel, {"stick":  {"colorscheme": "magentaCarbon", "radius": 0.3}})
    view.addStyle(sel, {"sphere": {"colorscheme": "magentaCarbon", "opacity": 0.5}})
    view.addResLabels(sel, {"fontSize": 12, "backgroundOpacity": 0.7})

    view.zoomTo()
    return view.show()


