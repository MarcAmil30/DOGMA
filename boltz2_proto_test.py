def save_structure(structure, path):
    """Save a proto_tools structure to disk. Format is inferred from the file extension."""
    text, fmt = get_structure_text(structure)
    # if you asked for .pdb but the library only gave .cif (or vice versa), warn
    ext = path.rsplit(".", 1)[-1].lower()
    if ext not in (fmt, "cif" if fmt == "cif" else "pdb"):
        print(f"note: structure exported as {fmt}, but you named the file .{ext}")
    with open(path, "w") as f:
        f.write(text)
    print(f"saved -> {path}")

